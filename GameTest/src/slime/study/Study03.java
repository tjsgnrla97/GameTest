package slime.study;

public class Study03 {

	public static void main(String[] args) {
		int a;
		a=1;
		int b=3;
		System.out.println(a);
		System.out.println(b);
		b++;
		System.out.println(b);
		b--;
		System.out.println(b);
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
	}

}
